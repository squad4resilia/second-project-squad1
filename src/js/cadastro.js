function confirmsenha() {
    let primeiro = document.getElementById("senhaum").value;
    let segundo = document.getElementById("senhadois").value;

    if (primeiro != segundo) {
        alert("Erro! Senhas diferentes!")
    }
}

function enviar() {
    let nome = document.getElementById("nome");
    let email = document.getElementById("email");
    let senha1 = document.getElementById("senhaum");
    let senha2 = document.getElementById("senhadois");
    let rg = document.getElementById("RG");
    let cepcerto = document.getElementById("cep");

    if (nome.value == "" || email.value == "" || senha1.value == "" || senha2.value == "" || rg.value == "" || cepcerto.value == "") {
        alert("Erro, algo está faltando.");
    } else {
        alert("Cadastro realizado com sucesso.");
    }
}
function validaRG() {
    let rg = document.getElementById("RG");
    if (rg.value.length < 9 || rg.value.length > 9) {
        alert("RG precisa de 9 caracteres");
    }
}

